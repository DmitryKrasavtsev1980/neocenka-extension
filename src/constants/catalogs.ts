/**
 * Получить название типа недвижимости по коду
 */
export function getRealEstateTypeName(code: string): string {
  return REAL_ESTATE_TYPES[code] || code;
}

/**
 * Получить название материала стен по коду
 */
export function getWallMaterialName(code: string): string {
  // Пытаемся найти точное совпадение
  if (WALL_MATERIALS[code]) {
    return WALL_MATERIALS[code];
  }

  // Пробуем убрать ведущий ноль (коды в CSV могут быть 061... вместо 61...)
  if (code.startsWith('0')) {
    const normalizedCode = code.substring(1);
    if (WALL_MATERIALS[normalizedCode]) {
      return WALL_MATERIALS[normalizedCode];
    }
  }

  // Пробуем добавить ведущий ноль
  const withZero = '0' + code;
  if (WALL_MATERIALS[withZero]) {
    return WALL_MATERIALS[withZero];
  }

  return code;
}

/**
 * Получить назначение по коду (определяет тип объекта автоматически)
 */
export function getPurposeName(code: string, realEstateTypeCode?: string): string {
  // Если известен тип объекта, используем соответствующий справочник
  if (realEstateTypeCode) {
    if (realEstateTypeCode === '002001002000') {
      // Здание
      return BUILDING_PURPOSES[code] || LAND_USE_TYPES[code] || code;
    }
    if (realEstateTypeCode === '002001003000') {
      // Помещение
      return ROOM_PURPOSES[code] || code;
    }
    if (realEstateTypeCode === '002001001000') {
      // Земельный участок
      return LAND_USE_TYPES[code] || code;
    }
  }

  // Если тип не известен, пробуем определить по коду
  if (code.startsWith('204')) {
    return BUILDING_PURPOSES[code] || code;
  }
  if (code.startsWith('206')) {
    return ROOM_PURPOSES[code] || code;
  }
  if (code.startsWith('214')) {
    return LAND_USE_TYPES[code] || code;
  }

  return code;
}

